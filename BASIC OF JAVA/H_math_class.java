// The Math class in Java is a built-in class that provides methods for performing mathematical calculations.

public class H_math_class {

    public static void main(String[] args) {
        
        System.out.println(Math.max(5, 6));       //tells the largest value
        System.out.println(Math.random());             // gives a random value between 0 and 1
        System.out.println((int)(Math.random()*100));  // now it will give randon value from 0 to 99
    }
}
